    private void loadData() {
        try {
            File file = new File("data.txt"); //create file
            file.createNewFile();//if not exit
            FileReader f = new FileReader("data.txt");
            StringBuffer sb = new StringBuffer();
            while (f.ready()) {
                char c = (char) f.read();
                if (c == '-') {
                    String prodarray[] = sb.toString().split(",");
                    //System.out.println(prodarray[2]);
                    prodlist.add(new Product(prodarray[0], Double.parseDouble(prodarray[1]), prodarray[2]));
                    sb = new StringBuffer();
                } else {
                    sb.append(c);
                }
            }
        } catch (IOException e) {
            return;
        }
        dtm.setRowCount(0);//reset data model
        for (int i = 0; i < prodlist.size(); i++) {
            Object[] objs = {prodlist.get(i).prodname, prodlist.get(i).prodprice, prodlist.get(i).prodcat,};
            dtm.addRow(objs);
        }
    }

    private void writeData() {
        try (FileWriter f = new FileWriter("data.txt")) {
            StringBuilder sb = new StringBuilder();
            for (int i = 0; i < prodlist.size(); i++) {
                sb.append(prodlist.get(i).prodname + "," + prodlist.get(i).prodprice + "," + prodlist.get(i).prodcat + "-");
            }
            f.write(sb.toString());
            f.close();
        } catch (IOException e) {
            return;
        }
        dtm.setRowCount(0);//reset data model
        for (int i = 0; i < prodlist.size(); i++) {
            Object[] objs = {prodlist.get(i).prodname, prodlist.get(i).prodprice, prodlist.get(i).prodcat};
            dtm.addRow(objs);
        }
    }

