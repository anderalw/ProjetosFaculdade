    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // Delete element
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Delete this data", "Delete", dialogButton);
        if (dialogResult == 0) {
            prodlist.remove(row);

            clearField();
            writeData();
        } else {
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // Update element
        String updatedprodname = jTextProdName.getText();
        double updatedprodprice = Double.parseDouble(jTextProdPrice.getText());
        String updatedprodcat = jCBProdCategory.getSelectedItem().toString();
        prodlist.get(row).prodname = updatedprodname;
        prodlist.get(row).prodprice = updatedprodprice;
        prodlist.get(row).prodcat = updatedprodcat;

        writeData();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // Search element
        String input = JOptionPane.showInputDialog(this, "Search Product Name:");
        for (int i = 0; i < prodlist.size(); i++) {
            if (prodlist.get(i).prodname.equalsIgnoreCase(input)) {
                JOptionPane.showMessageDialog(btnSearch, "Found!!!", "Search Product", 2);
                jTextProdName.setText(prodlist.get(i).prodname);
                jTextProdPrice.setText(String.valueOf(prodlist.get(i).prodprice));
                String location = prodlist.get(i).prodcat;
                for (int j = 0; j < (jCBProdCategory.getItemCount()); j++) {
                    if (jCBProdCategory.getItemAt(i).equalsIgnoreCase(location)) {
                        jCBProdCategory.setSelectedIndex(i);
                    }
                }
                return;//Quit after found
            }
        }
        JOptionPane.showMessageDialog(btnSearch, "Not Found!!!", "Search Product", 2);
    }//GEN-LAST:event_btnSearchActionPerformed

