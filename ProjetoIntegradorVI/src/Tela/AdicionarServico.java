    public void setarServicoNoCombo() {

        if (verificadorListaServico == false) {
            TipoServicoControle tsc = new TipoServicoControle();
            listaServico = tsc.listar(servico);
            verificadorListaServico = true;
        }

        TipoServico servicoSelecionado = listaServico.get(servico.getSelectedIndex());
        preco.setText(String.valueOf(servicoSelecionado.getValor()));

    }

    public int setarClienteNoCombo() {
        Cliente cliente = null;
        if (this.verificadorListaCliente == false) {
            ClienteControle cc = new ClienteControle();
            this.listaCliente = cc.listar(this.cliente);

            this.verificadorListaCliente = true;

            cliente = this.listaCliente.get(this.cliente.getSelectedIndex());

        } else {
            cliente = this.listaCliente.get(this.cliente.getSelectedIndex());
            this.setarAnimais(cliente.getId());
        }
        return cliente.getId();

    }

    public void setarAnimais(int idDono) {
        this.listaAnimal = null;
        AnimalControle ac = new AnimalControle();
        this.listaAnimal = ac.listar(idDono);

        String[] animais = new String[this.listaAnimal.size()];
        int cont = 0;
        for (Animal animal : this.listaAnimal) {
            animais[cont++] = animal.getNome();
        }

        this.animal.setModel(new javax.swing.DefaultComboBoxModel(animais));

    }

