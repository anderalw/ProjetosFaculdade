    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        AnimalControle ac = new AnimalControle();
        ServicoControle sc = new ServicoControle();
        ClienteControle cc = new ClienteControle();

        Animal animal = this.listaAnimal.get(this.animal.getSelectedIndex());
        TipoServico servico = this.listaServico.get(this.servico.getSelectedIndex());

        Servico s = new Servico();

        s.setData(data.getText());
        s.setHorario(horario.getText());

        char[] custoE = null;
        for (int i = 0; i < custo.getSelectedItem().toString().length(); i++) {
            custoE = custo.getSelectedItem().toString().toCharArray();
        }
        String custoC = "";
        for (int i = 0; i < 2; i++) {
            custoC += custoE[i];
        }

        float custoCorreto = (Float.parseFloat(preco.getText()) / 100) * Float.parseFloat(custoC);

        s.setCusto(custoCorreto);
        s.setPrecoServico(Float.parseFloat(preco.getText()));
        s.setId_animal(animal.getId());
        s.setNomeAnimal(animal.getNome());
        Cliente dono = cc.buscarUm(animal.getIdDono());
        s.setNomeDonoAnimal(dono.getNome());
        s.setId_tipoServico(servico.getId_servico());
        s.setNomeServico(servico.getNome());
        s.setFinalizado(false);
        sc.inserir(s);

        JOptionPane.showMessageDialog(null, "Serviço cadastrado com sucesso!");
        int dec = JOptionPane.showConfirmDialog(null, "Deseja adicionar outro serviço?");
        if (dec == 0) {

            data.setText("");

        } else {
            this.setVisible(false);
        }
        this.servicoTela.listarTabela();
    }//GEN-LAST:event_cadastrarActionPerformed

